    <h2 class="card-title">Purchasing</h2>
    <p class="card-text">Puchasing merupakan salah satu fungsi yang sangat penting dalam Manajemen Material, Selain dilibatkan dalam pembelian Material untuk kegunaan Produksi, Purchasing juga bertugas dalam pencarian dan pembelian mesin-mesin produksi, peralatan dan perlengkapan produksi berserta fasilitas-fasilitas lainnya yang mendukung kelancaran proses produksi. <br>Kata “Purchasing” berasal dari bahasa Inggris dan sering digunakan dalam Industri-industri luar negeri maupun dalam negeri. Jika diterjemahkan langsung, maka Purchasing dapat diartikan sebagai “Pembelian” dalam bahasa Indonesia. Jadi pada dasarnya, Purchasing adalah suatu proses pencarian sumber dan pemesanan barang atau jasa untuk kegiatan produksi.</p><p>Sasaran Utama Purchasing adalah untuk menjaga ketersediaan dan stabilitas pasokan material dan juga mengurangi biaya-biaya terkaitnya sehingga biaya pembuatan barang jadi dan ditekan seminimal mungkin.</p>
    <h3>Prosedur dalam Proses Purchasing</h3>
      <p>Prosedur dibawah ini merupakan urutan langkah-langkah dalam proses Purchasing (proses pembelian).</p>
        <h4>1. Mememahami Kebutuhan dan Menerima Permintaan dari Pihak yang membutuhkan</h4>
          <p>Langkah pertama dalam proses Purchasing adalah mengerti dan memahami kebutuhan dari pihak yang membutuhkan barang dan jasa tersebut. Pihak atau bagian yang membutuhkan tersebut akan mengajukan permintaan pemesanan atau pembelian dengan dokumen tertulis seperti Purchase Requisition Form (Formulir Permintaan Pembelian) yang telah disetujui oleh kepala bagian ataupun Manajer terkait. Formulir Permintaan Pembelian harus tertera jelas mengenai barang yang diinginkan seperti spesifikasi material, jumlah yang diinginkan, tanggal diperlukan dan pemasok yang disarankan.</p>
        <h4>2. Pemilihan Pemasok</h4>
          <p>Proses pemilihan Pemasok atau Supplier biasanya terdiri dari dua aspek dasar, yaitu melakukan pencarian terhadap semua pemasok potensial dan membuat daftar semua pemasok yang telah diidentifikasi. Informasi-informasi mengenai Pemasok pada umumnya bisa didapatkan dari beberapa sumber seperti direktori perdagangan, iklan, email rekomendasi diri dari pemasok itu sendiri, menghadiri pameran, partisipasi di konvensi industri ataupun saran dari asosiasi dan forum bisnis. Semakin banyak sumber yang kita identifikasikan dapat membantu kita dalam menentukan pemasok mana yang paling tepat. Hal yang perlu diperhatikan dalam memilih pemasok adalah harga bukanlah satu-satunya pertimbangan dalam mendapatkan Pemasok yang tepat. Karena selain harga, beberapa poin penting yang harus dipertimbangkan seperti kemampuan dalam menyediakan jumlah yang dibutuhkan, kualitas produk yang dibutuhkan, kemampuan finansial perusahaan pemasok dan lain sebagainya. Jika semua poin yang disebutkan sama, maka Hargalah yang menjadi penentu dalam pemilihan Pemasok.</p>
        <h4>3. Melakukan Pemesanan</h4>
          <p>Setelah menetapkan Pemasok/Supplier mana yang dipilih, maka langkah selanjutnya adalah melakukan pemesanan atau dalam industri biasanya disebut dengan “Purchase Order” atau “PO”. Purchase Order pada dasarnya adalah sebuah dokumen yang dikirimkan ke Pemasok untuk memasokan barang atau jasa yang dibutuhkan. Pada umumnya, dokumen atau surat Purchase Order (PO) terdiri dari 6 salinan, masing-masing salinan tersebut dituju kepada Pemasok, pihak pemohon (requestor), Petugas Gudang, bagian akuntansi, bagian inspeksi dan satu salinan lagi untuk bagian Purchasing sendiri sebagai arsip.</p>
        <h4>4. Menindaklanjuti dan memantau perkembangan Pesanan</h4>
          <p>Setelah melakukan Pemesanan dan Pemasok telah mengetahui apa yang dibutuhkan, maka prosedur Purchasing selanjutnya adalah melakukan pemantauan perkembangan pesanan tersebut atau biasanya disebut dengan “Follow-up” di Industri. Prosedur ini diperlukan agar Pemasok dapat menyediakan serta mengirimkan barang atau jasa yang dibutuhkan tersebut tepat pada waktu dan jumlah yang dijanjikan. Petugas Purchasing biasanya akan menanyakan ke Pemasok mengenai perkembangan barang dan jasa yang dibutuhkan tersebut melalui Email, telepon atau kunjungan langsung ke kantor/pabrik pemasok.</p>
        <h4>5. Penerimaan Barang dan Pemeriksaan</h4>
          <p>Bagian Penerimaan akan menerima barang yang dipasok oleh Pemasok dan mencocokan jumlah dengan dokumen Purchase Order. Bagian Purchasing akan diberitahukan tentang ketibaan barang tersebut dan juga hasil dari pemeriksaannya.</p>
        <h4>6. Pembayaran Faktur</h4>
          <p>Setelah barang yang dipesan tersebut diterima dengan kondisi yang memuaskan dan sesuai dengan permintaan, Faktur tagihan akan diperiksa sebelum disetujui. Setelah Faktur diperiksa dan disetujui maka pembayaran pun dilakukan sesuai dengan perjanjian yang ditetapkan.</p>
        <h4>7. Pemiliharaan Dokumen Pembelian</h4>
          <p>Sebagian besar pembelian di perusahaan Industri merupakan pembelian yang berulang atau Repeat Order. Oleh karena itu, dokumen-dokumen penting pada pembelian sebelumnya merupakan panduan untuk pembelian selanjutnya. Dari Dokumen-dokumen tersebut, Petugas Purchasing ataupun Manajemen dapat lebih jelas mengetahui pemasok mana yang baik dan tepat untuk pemesanan atau pembelian pada masa yang akan datang.</p>
        <h4>8. Memelihara dan Menjaga hubungan dengan Pemasok</h4>
          <p>Hubungan antara Perusahaan yang bersangkutan dengan Pemasok harus dipelihara dan dijaga dengan baik. Hubungan ini harus dijalin dalam bentuk saling percaya dan itikad baik untuk saling membantu dan menghargai. Hubungan baik akan bermanfaat dan menguntungkan kedua pihak baik bagi pembeli maupun pemasok.</p>

                    
